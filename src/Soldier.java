public class Soldier{
    private String difficulty;
    private int hp;
    private int speed;
    private String projectile;
    private int x;
    private int y;
    public Soldier(String difficulty, int hp, int speed, String projectile,int x, int y) {
        this.difficulty = difficulty;
        this.hp = hp;
        this.speed = speed;
        this.projectile = projectile;
        this.x = x;
        this.y = y;
    }
}